// Math.random(): 0~1 소수 랜덤 반환
console.log(Math.random())

// Math.round(): 반올림해서 정수형으로 반환
console.log(Math.round(1.279)) // 1
console.log(Math.round(1.679)) // 2

// Math.ceil(): 올림해서 정수형으로 반환
console.log(Math.ceil(1.279)) // 2
console.log(Math.ceil(1.679)) // 2

// Math.floor(): 내림해서 정수형으로 반환
console.log(Math.floor(1.279)) // 1
console.log(Math.floor(1.679)) // 1

// 0~9 랜덤 반환
console.log(Math.floor(Math.random()*10))